func TestTransformMarkdown(t *testing.T) {
	t.Parallel()

	raw := `## agh session list

List all active sessions

### Synopsis

List all sessions currently tracked by the AGH daemon.

` + "```" + `
agh session list [flags]
` + "```" + `

### Options

` + "```" + `
  -h, --help   help for session list
` + "```" + `

### SEE ALSO

* [agh session](agh_session.md) - Manage sessions

###### Auto generated by spf13/cobra on 15-Apr-2026
`

	result := TransformMarkdown(raw, "agh session list")

	// Verify frontmatter is present.
	if !strings.HasPrefix(result, "---\n") {
		t.Error("result should start with YAML frontmatter delimiter")
	}
	if !strings.Contains(result, `title: "agh session list"`) {
		t.Error("frontmatter should contain title")
	}
	if !strings.Contains(result, `description: "List all active sessions"`) {
		t.Error("frontmatter should contain description")
	}

	// Verify boilerplate is stripped.
	if strings.Contains(result, "Auto generated") {
		t.Error("auto-generated footer should be stripped")
	}
	if strings.Contains(result, "### SEE ALSO") {
		t.Error("SEE ALSO section should be stripped")
	}

	// Verify .md links are rewritten.
	if strings.Contains(result, ".md)") {
		t.Error("should not contain .md links")
	}

	// Verify content is preserved.
	if !strings.Contains(result, "### Synopsis") {
		t.Error("synopsis section should be preserved")
	}
	if !strings.Contains(result, "### Options") {
		t.Error("options section should be preserved")
	}
}

